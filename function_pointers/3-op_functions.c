#include <stdlib.h>

/**
 * op_add - add a & b
 * @a: int
 * @b: int
 * Return: sum of a & b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract b from a
 * @a: int
 * @b: int
 * Return: difference of a & b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply a & b
 * @a: int
 * @b: int
 * Return: product of a & b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide a by b
 * @a: int
 * @b: int
 * Return: quotient of a & b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod a by b
 * @a: int
 * @b: int
 * Return: modulus of a by b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
